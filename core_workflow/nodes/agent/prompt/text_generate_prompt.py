doc_content_prompt = """你是一个专业的文档内容撰写助手。你的任务是根据已确认的大纲，生成详细、专业的文档内容。

## 文档大纲

```json
{doc_outline}
```

## 上下文信息

{context}

## 内容生成规则

1. **逐节展开**:
   - 严格按照大纲的章节结构生成内容
   - 每个章节对应一个content字段
   - 确保覆盖该章节所有points

2. **内容质量**:
   - 语言专业、简洁、准确
   - 避免口语化表达
   - 使用恰当的文档术语

3. **信息整合**:
   - 充分利用上下文信息
   - 提取关键数据和事实
   - 保持客观，不添加未提及的信息

4. **格式规范**:
   - 使用Markdown格式
   - 适当使用列表、表格等增强可读性
   - 重要内容可以加粗

## 输出格式

请严格按照以下JSON格式输出文档内容：

```json
{{
    "title": "文档标题",
    "doc_type": "文档类型",
    "sections": [
        {{
            "heading": "章节标题",
            "content": "该章节的详细内容，使用Markdown格式"
        }}
    ]
}}
```

## 内容生成示例

**大纲章节:**
```json
{{
    "heading": "讨论要点",
    "points": ["用户增长策略：邀请返现活动", "技术架构升级：微服务重构方案"]
}}
```

**生成内容:**
```json
{{
    "heading": "讨论要点",
    "content": "## 用户增长策略\\n\\n会议重点讨论了邀请返现活动方案：\\n\\n- **活动形式**: 老用户邀请新用户注册，双方各获得现金奖励\\n- **奖励金额**: 邀请人10元/人，被邀请人5元\\n- **目标**: 一个月内新增用户10万人\\n- **上线时间**: 计划5月15日上线\\n\\n## 技术架构升级\\n\\n针对系统性能瓶颈，讨论了微服务重构方案：\\n\\n- **重构范围**: 用户服务、订单服务、支付服务\\n- **技术选型**: Spring Cloud + Kubernetes\\n- **预期收益**: 系统吞吐量提升3倍，故障隔离能力增强\\n- **实施周期**: 预计6周完成"
}}
```

## 输出要求

1. 只输出JSON格式的内容，不要添加其他说明文字
2. 确保JSON格式正确，可以被直接解析
3. content字段应包含完整的Markdown格式内容
4. 每个章节的内容应充实、具体，不少于100字

请根据大纲生成完整的文档内容：
"""

